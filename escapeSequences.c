#include <stdio.h>

int main() {

    /*

    Escape Sequences

    */

    char a[] = "\a"; // \a : Alert (Beep, Bell)

    char b[] = "\b"; // \b : Backspace

    char e[] = "\e"; // \e : Escape character

    char f[] = "\f"; // \f : Formfeed Page Break

    char n[] = "\n"; // \n : Newline

    char r[] = "\r"; // \r : Carriage Return

    char t[] = "\t"; // \t : Horizontal Tab

    char v[] = "\v"; // \v : Vertical Tab

    char backslash[] = "\\"; // \\ : Backslash

    char apostrophe[] = "\'"; // \' : Apostrophe

    char doubleQuotationMark[] = "\""; // \" : Double Quotation Mark

    char questionMark[] = "\?"; // \? : Question Mark

    
}